// Tipos de datos básicos de C++: Tipos de variables

#include<iostream>

using namespace std;

int main(){
	
	//Varibales: Enteros
	// Operador de asignación =
	int entero =15;
	
	cout<<entero<<endl;
	
	// Flotante: numeros decimales
	float flotante = 15.45;
	
	cout<<flotante<<endl;
	
	// Double: sirve para asignar numeros más extensos
	double doble = 16.4587;
	
	cout<<doble<<endl;
	
	//Varible de tipo caracter: Char
	char letra ='a';
	cout<<letra<<endl;
	
	
	return 0;
}
